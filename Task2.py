""" Измерены значения IQ выборки студентов,
обучающихся в местных технических вузах:
131, 125, 115, 122, 131, 115, 107, 99, 125, 111.
Известно, что в генеральной совокупности IQ распределен нормально.
Найдите доверительный интервал для математического ожидания с надежностью 0.95."""


import numpy as np
from scipy.stats import t

# Значения IQ выборки
iq_values = [131, 125, 115, 122, 131, 115, 107, 99, 125, 111]

# Размер выборки
n = len(iq_values)

# Среднее значение выборки
mean = np.mean(iq_values)

# Стандартное отклонение выборки
std_dev = np.std(iq_values, ddof=1)

# Критическое значение t-распределения для заданного уровня доверия и (n-1) степеней свободы
t_critical = t.ppf(0.975, df=n-1)

# Вычисление доверительного интервала
margin_of_error = t_critical * (std_dev / np.sqrt(n))
confidence_interval = (mean - margin_of_error, mean + margin_of_error)

# Вывод результатов
print("Доверительный интервал (0.95):", (round(confidence_interval[0], 2), round(confidence_interval[1], 2)))

"""вывод
    Доверительный интервал (0.95): (110.56, 125.64)
"""